package controlstatement;

public class IfElseIfStatement {
	public static void main(String[]args) {
		int colorvalue=2;
		if(colorvalue==1) {
			System.out.println("Color red");
		}
		else if(colorvalue==2) {
			System.out.println("Color black");
			
		}
		else {
			System.out.println("Color orange");
			
		}
	}
}
